void Stack::Disp()
{
	// 表示用の変数
	List* dispList = List::topPtr;

	// エラーチェック
	if (dispList == nullptr)
	{
		std::cout << "データがありません。" << std::endl;
		return;
	}

	// 要素の個数分表示
	for (int i = 0; i < List::GetListCount(); i++)
	{
		dispList->OutputData();
		dispList = dispList->nextPtr;
	}
}


